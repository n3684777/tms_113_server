var status;
 
function start() {
        status = -1;
        action(1, 0, 0);
}

function action(mode, type, selection) {
        if (mode == -1) {
                cm.dispose();
        } else {
                if (mode == 0 && type > 0) {
                        cm.dispose();
                        return;
                }
                if (mode == 1)
                        status++;
                else
                        status--;
    
                if(status == 0) {
                        cm.sendOk("對於那些擁有偉大成就和堅定決心的冒險者來說，#b 最終目的地 #k 就在大門前。 大門一次只接受#r 兩個隊伍 #k，因此請確保您的隊伍在穿過大門時已準備好。");
                        cm.dispose();
                }
        }
}
